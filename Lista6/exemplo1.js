
function soma(a, b){
    return a + b
}
function subtracao(a, b){
    return a - b
}
function multiplicacao(a, b){
    return a * b
}
function divisao(a, b){
    return a / b
}
let r1 = soma(1, 2)
let r2 = subtracao(3, 4)
let r3 = multiplicacao(5, 6)
let r4 = divisao(7, 8)

console.log(`A soma é ${r1}`)
console.log(`A subtração é ${r2}`)
console.log(`A multiplicação é ${r3}`)
console.log(`A divisão é ${r4}`)
